	public static void sendSound(Player p, String sound) {
		sound = sound.toUpperCase();
		if (!contains(sound))
			if (sounds.containsKey(sound) && !sounds.get(sound).isEmpty())
				sound = sounds.get(sound);
		if (contains(sound)) {
			p.playSound(p.getLocation(), Sound.valueOf(sound), 2, 2);
		} else {
			System.err.println("Sound nicht gefunden: " + sound);
		}
	}

	private static boolean contains(String sound) {
		for (Sound s : Sound.values())
			if (s.name().equals(sound))
				return true;
		return false;
	}

